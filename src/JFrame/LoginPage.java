    public void login() {
        String name = txt_username.getText();
        String pwd  = txt_password.getText();
    
        try {
           Class.forName("com.mysql.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/Library_msj","root","G1e2a3r4++-098");
           PreparedStatement pst = con.prepareStatement("Select * from users where username = ? and userpassword=?");
            pst.setString(1,name);
          pst.setString(2,pwd);
          
          ResultSet rs = pst.executeQuery();
            if (rs.next()) {
              JOptionPane.showMessageDialog(this, "login successful");
              HomePageGlobal home = new HomePageGlobal();
              home.setVisible(true);
              this.dispose();
              
                           
            }else{
             JOptionPane.showMessageDialog(this, "Incorrect username or password");   
            }
        } catch (Exception e) {
           e.printStackTrace();
        }
        
    }
     
      
